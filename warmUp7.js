// 3-calculate the average age of the follwing ages [13,14,13,15,16,17,19,13,16,15]
function average(array) {
	var total = 0;
	for (var i = 0; i < array.length; i++) {
		total = total[i-1] + [i]; 
	}
	return total;
}

